# 2. Python 的基本数据类型
# Python 有几种基本数据类型：整数 (int)、浮点数 (float)、字符串 (str)、布尔值 (bool)、列表 (list)、元组 (tuple)、字典 (dict)、集合 (set)。
# 整数：如 42
# 浮点数：如 3.14
# 字符串：如 "Hello"
# 布尔值：True 或 False
# 列表：可变序列，如 [1, 2, 3]
# 元组：不可变序列，如 (1, 2, 3)
# 字典：键值对，如 {"key": "value"}
# 集合：无序唯一元素，如 {1, 2, 3}

# 示例代码：演示各种数据类型
integer = 42
floating = 3.14
string = "Hello, World!"
boolean = True
my_list = [1, 2, 3]
my_tuple = (4, 5, 6)
my_dict = {"name": "Alice", "age": 25}
my_set = {1, 2, 3}

print("Integer:", integer)
print("Float:", floating)
print("String:", string)
print("Boolean:", boolean)
print("List:", my_list)
print("Tuple:", my_tuple)
print("Dict:", my_dict)
print("Set:", my_set)